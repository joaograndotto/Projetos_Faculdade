package ProjetoFinal;

public class Ingredientes {
    private String tipoIngred;
    private double quantIngred;
    private double valor;

    public String getTipoIngred() {
        return tipoIngred;
    }

    public void setTipoIngred(String tipoIngred) {
        this.tipoIngred = tipoIngred;
    }

    public double getQuantIngred() {
        return quantIngred;
    }

    public void setQuantIngred(int quantIngred) {
        this.quantIngred = quantIngred;
    }

    public double getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }


}

